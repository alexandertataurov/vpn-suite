export function translate(
  locale: SupportedLocale,
  key: string,
  params?: TranslationParams,
): string {
  const table = messages[locale] ?? messages.en;
  const template = table[key] ?? messages.en[key] ?? key;
  if (!params) return template;
  return template.replace(/\{(\w+)\}/g, (_match, name: string) => {
    if (Object.prototype.hasOwnProperty.call(params, name)) {
      const value = params[name];
      return value === undefined ? "" : String(value);
    }
    return `{${name}}`;
  });
}

export function normalizeLocale(input: string | null | undefined): SupportedLocale {
  const code = (input ?? "").trim().toLowerCase().slice(0, 2);
  return code === "ru" ? "ru" : "en";
}

export function resolveUserLocale(
  session: WebAppMeResponse | null | undefined,
  telegramLanguageCode: string | null | undefined,
): SupportedLocale {
  const sessionLocale = (session?.user?.locale ?? "").trim();
  if (sessionLocale) {
    const normalized = normalizeLocale(sessionLocale);
    if (normalized === "ru") return "ru";
    return "en";
  }

  const tgCode = (telegramLanguageCode ?? "").trim().toLowerCase().slice(0, 2);
  if (tgCode === "ru") return "ru";
  return "en";
}

